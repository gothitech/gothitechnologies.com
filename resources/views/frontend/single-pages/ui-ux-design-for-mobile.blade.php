<h3><strong>Use Micro-interactions</strong></h3>
<p>These are minor events that are part of a global system. They attract attention, communicate information, and reduce negative expectation impressions.</p>
<p>Some examples:</p>
<ul>
<li><a href="types-of-animations-for-mobile-apps.html" target="_blank" rel="noopener noreferrer">Animation after clicking</a> the icon;</li>
<li>Navigation while scrolling the content;</li>
<li>Event indication (changing the number of unread letters or notifications);</li>
<li>Data entry visualization. A plastic card picture is often added to a payment form. After filling in the form fields, the numbers automatically appear on the image;</li>
<li>Call to action (CTA).</li>
</ul>
<p><img class="alignnone size-full wp-image-3886" src="wp-content/uploads/2020/09/microinteraction.gif" alt="Microinteraction in mobile app ui" width="750" height="500" /></p>
<p>Micro-interactions work because they provide feedback and a clear understanding that the operation is completed or being resolved.</p>
<h3><strong>Adjust to the Audience</strong></h3>
<p>It would be best to know who your target audience is to tailor the design accordingly. Consider all the parameters known to you: age and gender, preferences and interests, the most popular services. Knowing this data, you will easily simulate users’ paths and determine their wishes and expectations from the interface in advance.</p>
<p>Answer the following question yourself: why does the user launch your app? Build the design based on the answer. Relatively speaking, if a person launches the app just to order <a href="how-to-make-a-food-delivery-app.html" target="_blank" rel="noopener noreferrer">food delivery services</a>, the appropriate functionality for this action should be placed right on the main page.</p>

