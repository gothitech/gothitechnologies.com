<h3><strong>Label UI Elements</strong></h3>
<p>If the icon is not labeled, chances are high users will not understand what it is meant for. Even icons with quite obvious functions (for example, a hamburger menu — a column of three horizontal lines) can puzzle users, despite the UX designers’ expectations.</p>
<p>Things will become even worse if your app features completely different icons since it will be even harder for users to understand what they mean. Consequently, label your interface elements so that users do not get confused.</p>
<h3><strong>Follow the Sequence</strong></h3>
<p>If users get to know your app gradually, that is the best way to give them time to comfortably adapt to it. Users follow a specific path and get solutions to their problems. Consistency increases conversion rates and provides a positive experience.</p>
<p>Here are a few examples of how consistency manifests itself in design:</p>
<ol>
<li><strong>Step by step registration.</strong> Users specify their email, enter a verification code from the mail, and get access to their personal account.</li>
<li><strong>Visual harmony.</strong> Typography, buttons, and blocks make an integral whole.</li>
<li><strong>The unity of style.</strong> App versions for Android and iOS maximally resemble each other, allowing users to avoid discomfort when switching from one system to another.</li>
</ol>
<p><img class="alignnone size-full wp-image-3885" src="wp-content/uploads/2020/09/app-sequence.jpg" alt="The unity of style in mobile design" width="1100" height="924" srcset="https://lvivity.com/wp-content/uploads/2020/09/app-sequence.jpg 1100w, https://lvivity.com/wp-content/uploads/2020/09/app-sequence-300x252.jpg 300w, https://lvivity.com/wp-content/uploads/2020/09/app-sequence-1024x860.jpg 1024w, https://lvivity.com/wp-content/uploads/2020/09/app-sequence-768x645.jpg 768w" sizes="(max-width: 1100px) 100vw, 1100px" /></p>
